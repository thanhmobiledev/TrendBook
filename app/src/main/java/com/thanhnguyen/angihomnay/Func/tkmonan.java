package com.thanhnguyen.angihomnay.Func;

public class tkmonan {
    String tenmon, solangoi;

    public tkmonan(String tenmon, String solangoi) {
        this.tenmon = tenmon;
        this.solangoi = solangoi;
    }
    public  tkmonan(){}

    public String getTenmon() {
        return tenmon;
    }

    public void setTenmon(String tenmon) {
        this.tenmon = tenmon;
    }

    public String getSolangoi() {
        return solangoi;
    }

    public void setSolangoi(String solangoi) {
        this.solangoi = solangoi;
    }
}
